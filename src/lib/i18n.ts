export type Locale = 'da' | 'en';

export function getLocale(url: URL): Locale {
  return url.pathname.startsWith('/en') ? 'en' : 'da';
}

export function localePath(locale: Locale, path: string): string {
  return locale === 'en' ? `/en${path}` : path;
}

